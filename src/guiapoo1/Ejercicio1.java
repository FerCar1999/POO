    private void jTextField4KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyPressed
        imageL.setIcon(new javax.swing.ImageIcon("src\\imagenes\\movimientoD.gif"));
        int tecla= evt.getKeyCode();
        if(tecla!=17){
            JOptionPane.showMessageDialog(this, "Tiene que mantener presionada la tecla espacio para tirar los dados");
            jTextField2.setText(" ");
            jTextField2.requestFocus();
        }
        else
        {
        for (int i = 0; i < 2; i++) {
            int Da= (int)(Math.random()*6)+1;
            jLabel4.setText(""+Da);
            dado=Da;
        }
        }
    }//GEN-LAST:event_jTextField4KeyPressed

    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        JOptionPane.showMessageDialog(this, dado);
        jTextField2.setText("");
        jTextField2.requestFocus();
        switch (dado)
        {
                case 1:
                imageL.setIcon(new javax.swing.ImageIcon("src\\imagenes\\uno.jpg"));
                break;
                case 2:
                imageL.setIcon(new javax.swing.ImageIcon("src\\imagenes\\dos.png"));
                break;
                case 3:
                imageL.setIcon(new javax.swing.ImageIcon("src\\imagenes\\tres.png"));
                break;
                case 4:
                imageL.setIcon(new javax.swing.ImageIcon("src\\imagenes\\cuatro.jpg"));
                break;
                case 5:
                imageL.setIcon(new javax.swing.ImageIcon("src\\imagenes\\cinco.png"));
                break;
                case 6:
                imageL.setIcon(new javax.swing.ImageIcon("src\\imagenes\\seis.jpg"));
                break;
            
        }
        
    }//GEN-LAST:event_jTextField4KeyReleased


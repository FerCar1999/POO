    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sTexto = jTextField1.getText();
        StringTokenizer stPalabras = new StringTokenizer(sTexto);
        while (stPalabras.hasMoreTokens()) {
          String sPalabra = stPalabras.nextToken();
          JOptionPane.showMessageDialog(this,sPalabra.substring(0,1));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cadena=jTextField2.getText();
        char charCadena='\n';
        int[] resul = new int[300];
        int numeroVeces=0, numeroRepetido=0;
        
        for(int i=0;i<cadena.length();i++)           
           resul[cadena.codePointAt(i)]++;
        
        for(int i=48;i<resul.length;i++){
            if(resul[i]>=numeroVeces){                
                numeroVeces=resul[i];
                numeroRepetido=i;
             }}
        
        for(int i=0;i<cadena.length();i++){
            if (numeroRepetido==cadena.codePointAt(i)){
                charCadena=cadena.charAt(i);                
            }
           
       }
        JOptionPane.showMessageDialog(this,"El numero mas repetido es = "+charCadena);
    }//GEN-LAST:event_jButton2ActionPerformed

